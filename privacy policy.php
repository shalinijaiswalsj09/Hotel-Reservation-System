Access to information

You have a right to access and correct personal information that we hold about you or to obtain a copy of it. We will make every endeavour to ensure your personal information is accurate and up to date therefore we ask that if there are changes to your information you should notify us directly. Any access request may be subject to a fee (not to exceed £10)  towards our costs in providing you with details of the information that we hold about you.  We may ask you to verify your identity and for more information about your request.  To obtain a copy of the personal information that we hold about you, please email us at webmaster@glhhotels.com.

