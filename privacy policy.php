
Privacy Policy

This Privacy Policy together with our Terms and Conditions and Cookie Policy sets out the basis on which any personal information that GLH Hotels Management (UK) Limited (“we”, “our”, “us”) collects from you, or that you provide to us, will be processed by us.  We are committed to protecting and respecting your privacy.

Information that we may collect from you

We may collect and process the following personal information about you:

• information that you provide via our website, our apps or our guest registration cards.
• information that you provide if you contact us through our general enquiry form.
• information that you provide when completing surveys that we use for research purposes.
• information you provide which we may use during your stay, or to enhance your stay with us.  This could include sensitive personal data, eg dietary requirements, health information and/or information about disabilities etc.
• details of transactions (including details of payment cards used) that you carry out through our website or apps.
• details of your visits to our website or apps, including, but not limited to, traffic data, location data, weblogs and other communication data, whether this is required for our own billing purposes or otherwise and/or the resources that you access.
• information that you provide when you enter a competition or promotion.
• when you report a problem with our website or one of our apps.
• information that you provide when registering to one of our programmes, including, but not limited to, registration to our website or apps, corporate account request, Booking Bonus registration and claim, e-newsletter subscription, registration to our Signature loyalty programme and Otium leisure membership.
• when you book one of our restaurants though the website or apps.
• information that you provide when submitting a Meetings and Events enquiry.
• Information passed to us by third parties.
• Information you may provide when ordering from our in-room concierge service.

Use of personal information

We may process personal information about you for the purposes of:

• providing you with our services.
• dealing with your booking, enquiries, complaints about our service, or other requests.
• administering your booking and other services.
• tailoring our services to your requirements and preferences.
• direct-marketing products and services, advising you of news and industry updates, events, promotions and competitions, reports and other information and carrying out market research campaigns.
• providing you with information about other products, special offers and services that you request from us or which we feel may interest you, where you have consented to be contacted for such purposes.  This may include products and services made available by selected partners and any member of our group of companies.
• conducting internal operations and analysis, including systems testing and statistical research.
• restructuring our business operations, eg we may need to transfer your information as a result of a sale or acquisition.
• Complying with laws, orders or regulations.

We may contact you by e-mail, text or telephone for these purposes.  Telephone calls may be recorded for training, monitoring and/or quality control.

Information that you enter into our booking forms

Information that you enter into our booking forms may be collected and stored by us so that we may contact you in relation to your booking/enquiry/attempted transaction. This may be used to send operational emails to you, or to call you to respond to the enquiry, or for the purposes outlined above, or to enquire as to why the transaction was not completed.


Social Media

Our website and apps provide plug-ins to social media websites, including Facebook, Twitter, Google, Yahoo and LinkedIn (see our website and apps for an up to date list).

If you make use of, or log-in to, the social media features on our website or apps, we may (depending on your privacy settings) access, use and store information about you, including, but not limited to:  your name, e-mail address, gender, location, profile, picture, contacts, and any other information you have chosen to make available.

To find out more about the reasons and extent to which social media sites collect and process your data, or to change your privacy settings, please refer to your social media provider’s privacy policy.


Marketing

Our website, apps and guest registration cards provide you with the opportunity to opt out of receiving  promotional and marketing information. You can exercise your right to prevent such processing by selecting the appropriate /opt out option. You can also exercise this right at any time by sending us an email at webmaster@glhhotels.com with your name, email address and, if you have made a booking with us, a confirmation number.  Alternatively, please contact  any of our hotels, or click  the “unsubscribe” button in our email communications.

Where we store your personal information

The information we collect from you may be transferred to, and stored at, a destination outside the European Economic Area (“EEA”).  It may also be processed by staff operating outside the EEA who work for us or one of our suppliers.  Such staff may be engaged in, among other things, the processing of your payment details and the provision of services.  By submitting your personal information, you agree to this transfer, storing or processing.  We will take all steps reasonably necessary to ensure that your information is treated securely and in accordance with this Privacy Policy.

Where we have given you (or where you have chosen) a password which enables you to access certain parts of our website, you are responsible for keeping this password confidential.  We ask you not to share a password with anyone.

Disclosure of your personal information

We will not sell your personal information to third parties without your prior consent. We may disclose your personal information to: any member of our group of companies, which includes our subsidiaries, our ultimate holding company and its subsidiaries; successors in title to our business; to suppliers and external agencies we engage to process information on our behalf; to third parties (including, but not limited to, professional indemnity insurers, brokers, new hotel owners/managers, auditors and other professional advisors); and where disclosure is required by law or any regulatory authority.

Apart from where disclosure is required by law or any regulatory authority, we will only disclose your personal information to our selected business partners and service providers:

• for the purpose of administrating your requests and any booking.
• for providing other services which you have requested.
• to communicate an offer of other goods and services, with your prior consent.

The internet

Given that the internet is a global environment, using the internet to collect and process personal information necessarily involves the transmission of information on an international basis.  Therefore, by browsing our website and communicating electronically with us, you acknowledge and agree to our processing of personal information in this way.

The transmission of information via the internet is not completely secure.  Although we do our best to protect personal information, we cannot guarantee the security of your information transmitted to our website; any transmission is at your own risk.  Once we have received your information, we will use strict procedures and security features to try to prevent unauthorised access or processing.

Our website may, from time to time, contain links to other websites which are outside of our control and are not covered by this Privacy Policy.  We do not accept any responsibility or liability for other sites’ privacy policies.  If you access other websites using the links provided, please check their policies before submitting any personal information.

